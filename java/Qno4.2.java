class Typecasting1
{
    public static void main(String a[])
    {
        // Type Explict
         double d=10.6;
         float  f=(float)d;
         long  l=(long)f;
         int   i=(int)l;
         short s=(short)i;
         byte  b=(byte)s;
                
        System.out.println(d);
        System.out.println(f);
        System.out.println(l);
        System.out.println(i);
        System.out.println(s);
        System.out.println(b);
    }
}
    
